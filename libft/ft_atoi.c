#include "libft.h"

int	ft_atoi(const char *str)
{
	int	result;
	int	sign;
	int	flag;

	result = 0;
	sign = 1;
	flag = 0;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (flag == 0)
		{
			if (*str == '-')
				sign = -sign;
			flag = 1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (result * sign);
}
